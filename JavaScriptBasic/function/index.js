function soma(x =0 , y = 0)  {
    const resultado = x + y;
    return resultado;
}

function subtracao(x = 0,y = 0) {
    const resultado = x - y;
    return resultado;
}

function multiplicacao(x = 0, y = 0) {
    const resultado = x * y;
    return resultado;
}

function divisao(x = 0, y = 0) {
    const resultado = x / y ;
    return resultado;
}


